    s2 = s2.replace("é", 'e')
    s2 = s2.replace("í", 'i')
    s2 = s2.replace("ó", 'o')
    s2 = s2.replace("ú", 'u')
    s2 = s2.replace("Ü", 'U')
    s2 = s2.replace("ü", 'u')
    s2 = s2.replace("Ñ", 'N')
    s2 = s2.replace("ñ", 'n')
    s2 = s2.replace(".", '')
    s2 = s2.replace(",", '')
    s2 = s2.replace(":", '')
    s2 = s2.replace("_", '')
    s2 = s2.replace("[", '')
    s2 = s2.replace("]", '')
    s2 = s2.replace("(", '')
    s2 = s2.replace(")", '')
    s2 = s2.replace("!", '')
    s2 = s2.replace("?", '')
    s2 = s2.replace("*", '')
    s2 = s2.replace("-", '')
    s2 = s2.replace("'", '')
    s2 = s2.replace('"', '')
    s2 = s2.replace("^", '')
    s2 = s2.replace("<", '')
    s2 = s2.replace(">", '')
    s2 = s2.replace("{", '')
    s2 = s2.replace("}", '')
    s2 = s2.replace("#", '')
